	CSEG SEGMENT PARA PUBLIC 'CODE'
		ASSUME CS:CSEG, DS:DSEG, SS:SSEG
		
	OUTPUT:	MOV AH, 2
			INT 21h
			MOV DX, ' '
			INT 21h
			RET
			
	NLINE:	MOV AH, 2
			MOV DX, 13
			INT 21h
			MOV DX, 10
			INT 21h
			RET
	
	PRINT:	MOV AH, 2
			MOV DI, 0
	PLOOP1:	MOV CX, 5
	PLOOP2:	MOV DL, Y[DI]
			CALL OUTPUT
			INC DI
			LOOP PLOOP2
			CALL NLINE
			CMP DI, 25
			JNZ PLOOP1
			RET
	

	START:	MOV AX, DSEG
			MOV DS, AX
	
	M1:		MOV BL, 'A'
			MOV SI, 0
			MOV DI, 0
			
	INIT:	MOV CX, 5
	ILOOP:	MOV AL, BL
			XCHG AL, Y[DI]
			INC BL
			INC DI
			LOOP ILOOP
			ADD SI, 5
			CMP SI, 25
			JNZ INIT
			
			CALL PRINT
			CALL NLINE
			
	M2:		MOV BP, 0
			MOV BX, 0
	TRANS:	MOV SI, 5
			MOV DI, 1
			MOV CX, 4
			SUB CX, BX
	TLOOP:	XCHG AL, Y[BP + DI]
			XCHG AL, Y[BP + SI]
			XCHG AL, Y[BP + DI]
			INC DI
			ADD SI, 5
			LOOP TLOOP
			INC BX
			ADD BP, 6
			CMP BX, 4
			JNZ TRANS
			
			CALL PRINT
		
	FINISH:	MOV AH, 4Ch
			INT 21h
		
