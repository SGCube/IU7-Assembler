PUBLIC M4

CSEG SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CSEG

M4 PROC
	PUSH BX
	PUSH BP

	MOV BP, SP
	MOV AX, [BP + 6]
	MOV BX, 10
	MOV CX, 0
	
MAINLP:
	MOV DL, 0
	DIV BX
	ADD DL, '0'
	INC CX
	PUSH DX
	CMP AX, 0
	JNZ MAINLP

	MOV AH, 2
PRINT:
	POP DX
	INT 21H
	LOOP PRINT
	
	POP BP
	POP BX
	RET 2
M4 ENDP

CSEG ENDS
	END